#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	size_t x;

	if (s1 == NULL || s2 == NULL)
		return (0);
	x = 0;
	while (s1[x] == s2[x] && s1[x] != '\0')
		x++;
	if (s1[x] == s2[x])
		return (1);
	else
		return (0);
}
