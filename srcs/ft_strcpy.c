#include "libft.h"

char	*ft_strcpy(char *dst, const char *str)
{
	int		x;

	x = 0;
	while (str[x] != '\0')
	{
		dst[x] = str[x];
		x++;
	}
	dst[x] = '\0';
	return (dst);
}
