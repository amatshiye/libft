#include "libft.h"

void	ft_putnbr(int n)
{
	long	nb;

	nb = (long)n;
	if (nb >= 0 && nb <= 9)
		ft_putchar(nb + '0');
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
}
